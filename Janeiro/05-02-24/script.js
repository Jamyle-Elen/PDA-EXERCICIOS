for (let i = 1; i <= 10; i++) {
  console.log(i);
}

let i = 1;
while (i <= 10) {
  console.log(i);
  i++;
}

// VANTAGENS
// flexibilidade no codigo
// simples alteração

// DESVANTAGENS
// while seria mais longo e requer uma inicialização manual da variavel.
// por ser mais extenso pode causar acidentalmente um loop infinito.